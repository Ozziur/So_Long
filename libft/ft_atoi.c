#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int				i;
	int				segno;
	unsigned int	n;

	i = 0;
	segno = 1;
	n = 0;
	while (nptr[i] == '\t' || nptr[i] == '\n' || nptr[i] == '\v'
		|| nptr[i] == '\f' || nptr[i] == '\r' || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			segno = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		n = n * 10 + (nptr[i] - 48);
		i++;
	}
	n = n * (unsigned int) segno;
	return (n);
}
